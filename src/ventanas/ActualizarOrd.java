    public void listCatProducto() throws SQLException{
        ArrayList<Productos>listCat;
        try{
            listCat=pdao.listCategoria(0);
            DefaultComboBoxModel combo=(DefaultComboBoxModel)comboCat.getModel();
            for(Productos prod: listCat){
                combo.addElement(prod.getCategoria());
            }
            comboCat.setModel(combo);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void listNombreProd() throws SQLException{    
    ArrayList<Productos>listNom;
    comboNombre.removeAllItems();
        try{
            listNom=pdao.listNombre(comboCat.getSelectedItem().toString());
            DefaultComboBoxModel combo=(DefaultComboBoxModel)comboNombre.getModel();
            for(Productos prod: listNom){
                combo.addElement(prod.getCategoria());
                listIDProd();
            }
            comboNombre.setModel(combo);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void listIDProd() throws SQLException{    
    ArrayList<Productos>listID;
        try{
            listID=pdao.listId(comboNombre.getSelectedItem().toString());
            for(Productos prod: listID){
                txtId.setText(String.valueOf(prod.getIdProducto()));
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void listIDProd2() throws SQLException{    
    ArrayList<Productos>listID;
        try{
            listID=pdao.listId((String) tblPedidos.getValueAt(tblPedidos.getSelectedRow(), 1));
            for(Productos prod: listID){
                txtId2.setText(String.valueOf(prod.getIdProducto()));
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void pedidos() throws SQLException{
    ArrayList<sp_muestrPedido> listPedido;
    try {
    listPedido =mpdao.consultaDatos(Integer.valueOf(txtOrden.getText()));
    DefaultTableModel table=(DefaultTableModel)tblPedidos.getModel();
    table.setRowCount(0);
    for(sp_muestrPedido mp: listPedido){
        table.addRow(new Object[]{mp.getCantidad(),mp.getNombre(),mp.getPrecioU(),mp.getPrecioT()});
    }
    tblPedidos.setModel(table);
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
