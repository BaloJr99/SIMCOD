public void tel() throws SQLException{
    ArrayList<TelefonoEmp> listTels;
    try {
    listTels = teld.consultaTel(Integer.valueOf(txtIdTel1.getText()));
    DefaultTableModel table=(DefaultTableModel)tblTelefonos.getModel();
    table.setRowCount(0);
    for(TelefonoEmp te: listTels){
        table.addRow(new Object[]{te.getTelefono()});
    }
    tblTelefonos.setModel(table);
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
