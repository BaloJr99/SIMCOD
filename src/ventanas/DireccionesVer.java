public void dir() throws SQLException{
    ArrayList<DireccionEmp> listPedido;
    try {
    listPedido =dempd.consultaDatos(Integer.valueOf(txtID.getText()));
    DefaultTableModel table=(DefaultTableModel)tblDirecciones.getModel();
    table.setRowCount(0);
    for(DireccionEmp de: listPedido){
        table.addRow(new Object[]{de.getCalle(),de.getNumero(),de.getColonia(),de.getCp(),de.getCiudad()});
    }
    tblDirecciones.setModel(table);
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
