    private void panEstadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panEstadoMouseClicked
        if(panEstado.getBackground().equals(Color.RED)){
            int producto,orden;
            producto=Integer.valueOf(txtIdP.getText());
            orden=Integer.valueOf(comboOrdenes.getSelectedItem().toString());
            PedidoDao pdao=new PedidoDao();
            Pedido pe=new Pedido(producto, orden);
            try {
                pdao.actualizaEstado(pe);
                JOptionPane.showMessageDialog(this, "Pedido listo");
                panEstado.setBackground(Color.green);
            } catch (SQLException ex) {
                Logger.getLogger(OrdenACoc.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_panEstadoMouseClicked

