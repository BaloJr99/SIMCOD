public void ordenes() throws SQLException{
    ArrayList<Ordenes> listOrden;
    try {
    listOrden=odao.muestraIdOrden(0);
    for(Ordenes o: listOrden){
        comboOrdenes.addItem(String.valueOf(o.getIdOrden()));
    }
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
public void pedidos() throws SQLException{
    ArrayList<Pedido> listPedido;
    try {
    listPedido =pDao.muestraNombre(Integer.valueOf(comboOrdenes.getSelectedItem().toString()));
    DefaultTableModel table=(DefaultTableModel)tblPedidos.getModel();
    table.setRowCount(0);
    for(Pedido pe: listPedido){
        table.addRow(new Object[]{pe.getNombre()});
    }
    tblPedidos.setModel(table);
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
public void info() throws SQLException{
    ArrayList<Pedido> listPedido;
    try {
    listPedido =pDao.muestraPedido((String) tblPedidos.getModel().getValueAt(tblPedidos.getSelectedRow(), 0));
    for(Pedido pe: listPedido){
        txtIdP.setText(String.valueOf(pe.getProducto_idProducto()));
        txtCantidad.setText(String.valueOf(pe.getCantidad()));
        if(pe.getEstado().equals("Listo")){
            panEstado.setBackground(Color.GREEN);
        }
        if(pe.getEstado().equals("En preparacion")){
            panEstado.setBackground(Color.RED);
        }
    }
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
