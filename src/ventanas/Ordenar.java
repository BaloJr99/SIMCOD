public void listCatProducto() throws SQLException{
        ArrayList<Productos>listCat;
        try{
            listCat=pdao.listCategoria(0);
            DefaultComboBoxModel combo=(DefaultComboBoxModel)comboCat.getModel();
            for(Productos prod: listCat){
                combo.addElement(prod.getCategoria());
            }
            comboCat.setModel(combo);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void listNombreProd() throws SQLException{    
    ArrayList<Productos>listNom;
    comboNombre.removeAllItems();
        try{
            listNom=pdao.listNombre(comboCat.getSelectedItem().toString());
            DefaultComboBoxModel combo=(DefaultComboBoxModel)comboNombre.getModel();
            for(Productos prod: listNom){
                combo.addElement(prod.getCategoria());
                listIDProd();
            }
            comboNombre.setModel(combo);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
public void listIDProd() throws SQLException{    
    ArrayList<Productos>listID;
        try{
            listID=pdao.listId(comboNombre.getSelectedItem().toString());
            for(Productos prod: listID){
                txtId.setText(String.valueOf(prod.getIdProducto()));
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }


public void cantidadIng() throws SQLException{
    ArrayList<SP_muestraIng> listIngredientes;
    try {
    listIngredientes =midao.consultaDatos(Integer.valueOf(spiCantidad.getValue().toString()),Integer.valueOf(txtId.getText()));
    DefaultTableModel table=(DefaultTableModel)tblIngredientes.getModel();
    //table.setRowCount(0);
    for(SP_muestraIng mi: listIngredientes){
        table.addRow(new Object[]{mi.getIdIngrediente(),mi.getIngrediente(),mi.getExistencia(),mi.getCantidad(),mi.getCantxprod(),mi.getCantNecesaria(),mi.getIdProducto(),mi.getDescripion()});
    }
    tblIngredientes.setModel(table);
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
public void noOrden() throws SQLException{
    ArrayList<Ordenes> noOrden;
    try {
    noOrden =oDao.muestraId(0);
    //table.setRowCount(0);
    for(Ordenes or: noOrden){
        int sum1=or.getIdOrden();
        int suma=sum1+1;
        txtIdOrden.setText(String.valueOf(suma));
    }
    } catch (SQLException ex) {
        System.out.println(ex);
    }
}
