    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if(listCantidad.getModel().getSize()==0){
            throw new MisExcepciones(22);
        }else{
        int idMes=0,idOrd=0,idRes=0,idCant=0,idIng=0;
        sp_insertaOrdenDao iod=new sp_insertaOrdenDao();
        sp_insertaPedidoDao ipd=new sp_insertaPedidoDao();
        idOrd=Integer.valueOf(txtIdOrden.getText());
        if(comboPara.getSelectedItem().toString().equals("Comer aqui")){
            idMes = idMesa;
        }
        if(comboPara.getSelectedItem().toString().equals("Llevar")){
            idMes=0;
        }
        sp_insertaOrden io= new sp_insertaOrden(idOrd,idMes,idRes);
        
        iod.insertaOrden(io);
        for(int i=0;i<listId.getModel().getSize();i++){
            idCant=Integer.valueOf(listCantidad.getModel().getElementAt(i).toString());
            idIng=Integer.valueOf(listId.getModel().getElementAt(i).toString());
            sp_insertaPedido ip=new sp_insertaPedido(idCant, idIng, idOrd);
                ipd.insertaPedido(ip);
                            JOptionPane.showMessageDialog(rootPane, "Registrado correctamente");
        }
        txtPrueba.setText(String.valueOf(listId.getModel().getSize()));
            }
        }catch (SQLException ex) {
                Logger.getLogger(Ordenar.class.getName()).log(Level.SEVERE, null, ex);
        }catch (MisExcepciones ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

